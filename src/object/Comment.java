package object;

public class Comment {
	private String articalID;
	private String avartaFB;
	private String title;
	private String creatDate;
	private String content;
	public Comment()
	{
		super();
	}
	public Comment(String articalID,String avartaFB,String title,String creatDate,String content)
	{
		super();
		this.setArticalID(articalID);
		this.setAvartaFB(avartaFB);
		this.setTitle(title);
		this.setCreatDate(creatDate);
		this.setContent(content);
	}
	public String getArticalID() {
		return articalID;
	}
	public void setArticalID(String articalID) {
		this.articalID = articalID;
	}
	public String getAvartaFB() {
		return avartaFB;
	}
	public void setAvartaFB(String avartaFB) {
		this.avartaFB = avartaFB;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCreatDate() {
		return creatDate;
	}
	public void setCreatDate(String creatDate) {
		this.creatDate = creatDate;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
}

